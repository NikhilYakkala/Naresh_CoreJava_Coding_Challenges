package com.CodingChallenges;

public class DefaultValueswithStaticKeyword {
	
	static int a;
	
	static float b;
	
	static char c;
	
	static boolean d;
	
	public static void main(String[] args) {
				
		System.out.println("int default Value = "+a);
		
		System.out.println("float default Value = "+b);
		
		System.out.println("character default Value = "+c);
		
		System.out.println("boolean default Value = "+d);
	}
	

}
